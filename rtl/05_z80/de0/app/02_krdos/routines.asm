; ПЕЧАТЬ ASCIIZ-строки по адресу DE
; ----------------------------------------------------------------------

PRNMSG: push    de
        push    af
PM1N:   ld      a, (de)
        inc     de
        and     a
        jr      z, PM0E
        rst     $08
        jr      PM1N
PM0E:   pop     af
        pop     de
        ret
